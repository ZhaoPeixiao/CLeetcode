#pragma once
#include<iostream>
#include<string>
#include<unordered_map>
using namespace std;

bool checkInclusion(string t, string s) {
    unordered_map<char, int> need, window;
    for (char c : t) {
        need[c]++;
    }
    int left = 0, right = 0;
    int valid = 0;
    while (right < s.size()) {
        char c = s[right];
        right++;
        // 进行窗口内数据的一系列更新
        if (need.count(c)) {
            window[c]++;
            if (window[c] == need[c]) {
                valid++;
            }
                
        }

        // 判断左侧窗口是否要收缩
        while (right - left >= t.size()) {
            // 在这里判断是否找到了合法的子串
            if (valid == need.size()) {
                return true;
            }
            char d = s[left];
            left++;
            // 进行窗口内数据的一系列更新
            if (need.count(d)) {
                if (window[d] == need[d]) {
                    valid--;
                }
                window[d]--;
            }
        }
    }
    // 未找到符合条件的子串
    return false;
}
